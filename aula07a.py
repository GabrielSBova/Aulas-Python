n1 = int(input('Um valor: '))
n2 = int(input('Outro valor: '))
soma = n1 + n2
mult = n1 * n2
div = n1 / n2
divInt = n1 // n2
pot = n1 ** n2
print('A soma é {}, o produto é {} e a divisão é {:.3f}'.format(soma, mult, div), end=' >>> ')
print('A divisão inteira é: {}, a potência é: {}'.format(divInt, pot))





